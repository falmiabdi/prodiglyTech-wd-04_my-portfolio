function openOffcanvas() {
  document.getElementById("offcanvas").classList.add("show");
}

function closeOffcanvas() {
  document.getElementById("offcanvas").classList.remove("show");
}

function openMenu() {
  document.getElementById("dropdownMenu").style.right = "0";
}

function closeMenu() {
  document.getElementById("dropdownMenu").style.right = "-100%";
}
